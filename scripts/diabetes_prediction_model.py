import numpy as np
import pandas as pd
from sklearn.preprocessing import StandardScaler
from sklearn.model_selection import train_test_split
from sklearn import svm
from sklearn.metrics import accuracy_score

"""Data collection and analysis
PIMA diabetes dataset          
"""

diabetes_dataset = pd.read_csv('/content/diabetes.csv')

#print first 5 rown from data
diabetes_dataset.head()

#getting the number of rows and columns from dat
diabetes_dataset.shape

#getting statisstical info of data
diabetes_dataset.describe()

#how many cases are there for diabetic examples and non diabetic examples
diabetes_dataset['Outcome'].value_counts()

#getting the mean from valus 0 and 1
diabetes_dataset.groupby('Outcome').mean()

#seperate data and labels
X = diabetes_dataset.drop(columns = 'Outcome', axis=1)
Y = diabetes_dataset['Outcome']

print(X)

print(Y)

"""data standardization"""

scaler = StandardScaler()

scaler.fit(X)

#transform this data
standardized_data = scaler.transform(X)

print(standardized_data)

X = standardized_data
Y = diabetes_dataset['Outcome']

print(X)
print(Y)

"""train test split"""

X_train, X_test, Y_train, Y_test = train_test_split(X,Y, test_size = 0.2, stratify=Y, random_state=2)

print(X.shape, X_train.shape, X_test.shape)

"""training the model"""

classifier = svm.SVC(kernel='linear')

#training the suport vector machin classifier
classifier.fit(X_train, Y_train)

"""evaluate model"""

#accurucy score on training data
X_train_prediction = classifier.predict(X_train)
training_data_accuracy = accuracy_score(X_train_prediction, Y_train)

print('Accuracy score of the training data : ', training_data_accuracy)

#accurucy score of test data
X_test_prediction = classifier.predict(X_test)
test_data_accuracy = accuracy_score(X_test_prediction, Y_test)

print('Accuracy score of the test data : ', test_data_accuracy)

input_data = (5,166,72,19,175,25.8,0.587,51)

# changing the input_data to numpy array
input_data_as_numpy_array = np.asarray(input_data)

# reshape the array as we are predicting for one instance
input_data_reshaped = input_data_as_numpy_array.reshape(1,-1)

# standardize the input data
std_data = scaler.transform(input_data_reshaped)
print(std_data)

prediction = classifier.predict(std_data)
print(prediction)

if (prediction[0] == 0):
  print('The person is not diabetic')
else:
  print('The person is diabetic')

